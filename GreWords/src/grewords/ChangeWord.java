    public boolean areOK(){
        if(wordTextField.getText().equals(""))
            return false;
        if(meaningTextArea.getText().equals(""))
            return false;
        if(exampleTextArea.getText().equals(""))
            return false;
        return true;
    }
    
    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if(areOK() == false){
            JOptionPane.showMessageDialog(null, "Please fill all the fields!");
            return ;
        }
        String word = wordTextField.getText(), meaning = meaningTextArea.getText();
        word = word.toLowerCase();
        String category = categoryComboBox.getSelectedItem().toString();
        String example = exampleTextArea.getText();
        welcome.dbManager.removeItem(word);
        System.out.println("ChangeWord Line 216, item removed!!!");
        ArrayList<String> strings = new ArrayList<String>();
        Long millis = System.currentTimeMillis();
        strings.add("word");
        strings.add(word);
        strings.add("meaning");
        strings.add(meaning);
        strings.add("type");
        strings.add(category);
        strings.add("example");
        strings.add(example);
        strings.add("viewed");
        if(fields == null)
            strings.add("1");
        else
            strings.add(fields.viewed);
        strings.add("lastseen");
        strings.add(millis.toString());
        welcome.dbManager.addItem(strings);
        System.out.println("The item added successfully!");
        String message;
        if(fields == null)
            message = "Word successfully added to the dictionary";
        else
            message = "Changes saved.";
        JOptionPane.showMessageDialog(this, message);
        welcome.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_saveButtonActionPerformed

