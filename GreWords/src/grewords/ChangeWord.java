    private void initComboBox(String category){
        categoryComboBox.removeAllItems();
        categoryComboBox.addItem("noun");
        categoryComboBox.addItem("pronoun");
        categoryComboBox.addItem("verb");
        categoryComboBox.addItem("adverb");
        categoryComboBox.addItem("adjective");
        if(category != null)
            categoryComboBox.setSelectedItem(category);
        else
            categoryComboBox.setSelectedItem("verb");
    }
    
    private void initFields(){
        if(fields != null){ // change request
            wordTextField.setText(fields.word);
            meaningTextArea.setText(fields.meaning);
            initComboBox(fields.category);
            exampleTextArea.setText(fields.example);
            saveButton.setText("Save");
        } else{ // add request
            wordTextField.setText("");
            meaningTextArea.setText("");
            initComboBox(null);
            exampleTextArea.setText("");
            saveButton.setText("Add");
        }
    }

